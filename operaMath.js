function soma (x,y){
    return x+y
}

function subtracao (x,y){
    return x-y
}

function divisao (x,y){
    return x/y
}

function multi(x,y){
    return x*y
}

console.log('O Resultado das operações são:','soma:'+soma(5,10),'subtracao:'+subtracao(10,8),'divisao:'+divisao(10,2),'multiplicação:'+multi(25,5))